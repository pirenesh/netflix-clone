// Helper function to search in our mock database
export const searchMockMovies = (query) => {
  if (!query) return [];
  const lowercaseQuery = query.toLowerCase();
  const results = [];
  const seenIds = new Set();

  Object.values(MOCK_MOVIES).forEach(categoryList => {
    categoryList.forEach(movie => {
      if (!seenIds.has(movie.id)) {
        const titleMatch = movie.title && movie.title.toLowerCase().includes(lowercaseQuery);
        const nameMatch = movie.name && movie.name.toLowerCase().includes(lowercaseQuery);
        const overviewMatch = movie.overview && movie.overview.toLowerCase().includes(lowercaseQuery);
        if (titleMatch || nameMatch || overviewMatch) {
          seenIds.add(movie.id);
          results.push(movie);
        }
      }
    });
  });

  return results;
};

// Helper function to get mock movie details
